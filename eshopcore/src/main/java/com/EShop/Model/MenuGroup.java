package com.EShop.Model;

public class MenuGroup {
    private  String ID;
    private  String Name;

    public  MenuGroup(){}

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }
}
